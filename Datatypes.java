public class Datatypes {
    public static void main(String[] args) {
        byte b = 8;
        System.out.println(b);
        char ch ='s';
        System.out.println(ch);
        int d = 15;
        System.out.println(d);
        float p = 17.2f;
        System.out.println(p);
        double a =123.456;
        System.out.println(a);

    }
}
